    private void tb_paquetesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_paquetesMouseClicked
        int column = tb_paquetes.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY()/tb_paquetes.getRowHeight();
        
        if(column == 4) {
            AvisoBorrarPaquete avisoBorrar = new AvisoBorrarPaquete();
        escritorio.removeAll();
        escritorio.repaint();
        avisoBorrar.setVisible(true);
        
        avisoBorrar.id = (int)tb_paquetes.getValueAt(row, 0);
       
        escritorio.add(avisoBorrar).setSize(400, 400);
        escritorio.moveToFront(avisoBorrar);
        
        } 
        if(column == 5) {
          PaquetesAgregar frmAgregar = new PaquetesAgregar();
        escritorio.removeAll();
        escritorio.repaint();
        frmAgregar.setVisible(true);
      frmAgregar.modo = 2;
      frmAgregar.id = (int)tb_paquetes.getValueAt(row, 0);
        escritorio.add(frmAgregar).setSize(400, 400);
        escritorio.moveToFront(frmAgregar);   
        }
            
        
    }//GEN-LAST:event_tb_paquetesMouseClicked


