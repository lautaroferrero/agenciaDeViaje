    public ListaPaquete() {
        initComponents();
         try {
            Conexion con = new Conexion("jdbc:mysql://localhost/agenciadeturismo", "root", ""); 
            PaqueteData pd = new PaqueteData(con);
            List<Paquete> listaPaquete = pd.listadoPaquete();
             modelo = (DefaultTableModel) tb_paquetes.getModel();
            tb_paquetes.setDefaultRenderer(Object.class, new Render());

            
            modelo.setRowCount(0);
            Object[] fila = new Object[modelo.getColumnCount()];

            for (int i = 0; i < listaPaquete.size(); i++) {
                fila[0] = listaPaquete.get(i).getId();
                fila[1] = listaPaquete.get(i).getTraslado().getPatente();
                fila[2] = listaPaquete.get(i).getTraslado().getTipoDeTransporte();
                fila[3] = listaPaquete.get(i).getAlojamiento().getDireccion();
                JButton btnBorrar = new JButton();
                btnBorrar.setText("Borrar");
                fila[4] = btnBorrar;
               
                JButton btnActualizar = new JButton();
                btnActualizar.setText("Actualizar");
                fila[5] = btnActualizar;
                
                modelo.addRow(fila);
            }
        } catch(Exception ex) {
            System.out.println(ex.getMessage());
        }
    }

